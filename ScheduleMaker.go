//Makes schedule
func Maker(s schedules){
	finalSchedule := [][]student{}
	hours := []int{8, 9, 10, 11, 12, 2, 3, 4, 5}; hm := 0
	for len(s.sList) > 0{
		index := hardestSchedule(s)
		block := []student{}
		if len(s.sList) > 0{ // creates list of up to four schedules which fit best together
			block = append(block, s.sList[index])
			s.sList = append(s.sList[:index], s.sList[index+1:]...)
			for len(s.sList) > 0 && len(block) < 4{  //		i := 0; i < 3; ++{
					n := bestFit(block, s)
					if n != -1 {
						block = append(block, s.sList[n])
						s.sList = append(s.sList[:n], s.sList[n+1:]...)

					}else{
						break
					}
			}

		}
		//prints time
		if hm == len(hours)*2 - 1 {
			hm = 0
		}
		if hm % 2 == 0{
			fmt.Print(hours[hm/2], ":00 ")

		}else{
			fmt.Print(hours[hm/2],":30 ")
		}
		hm++

		for i := 0; i < len(block);i ++{
			fmt.Print(block[i].name + " ")
		}
		fmt.Println()
		finalSchedule = append(finalSchedule, block)
	}
}

